'use client'

import { useEffect, useState } from 'react'
import { useRouter } from 'next/navigation'
import { createBrowserClient } from '@supabase/ssr'
import { signInWithGoogle } from '@/lib/auth'

export default function WorkspacePage() {
    const router = useRouter()
    const supabase = createBrowserClient(
        process.env.NEXT_PUBLIC_SUPABASE_URL!,
        process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
    )
    const [isLoading, setIsLoading] = useState(true)
    const [isSigningIn, setIsSigningIn] = useState(false)

    useEffect(() => {
        checkAuth()
    }, [])

    async function checkAuth() {
        try {
            const { data: { session } } = await supabase.auth.getSession()

            if (session) {
                // User is authenticated, check for workspaces
                const { data: workspaces } = await supabase
                    .from('workspaces')
                    .select('slug')
                    .limit(1)
                    .single()

                if (workspaces) {
                    // Redirect to first workspace
                    router.push(`/workspace/${workspaces.slug}/dashboard`)
                } else {
                    // No workspace, go to onboarding
                    router.push('/onboarding')
                }
            }
        } catch (error) {
            console.error('Auth check error:', error)
        } finally {
            setIsLoading(false)
        }
    }

    async function handleGoogleSignIn() {
        try {
            setIsSigningIn(true)
            const { error } = await signInWithGoogle()
            if (error) throw error
        } catch (error) {
            console.error('Sign in error:', error)
            setIsSigningIn(false)
        }
    }

    if (isLoading) {
        return (
            <div className="min-h-screen bg-black flex items-center justify-center">
                <div className="text-white text-xl">Loading...</div>
            </div>
        )
    }

    return (
        <div className="min-h-screen bg-black text-white flex items-center justify-center">
            <div className="max-w-md w-full mx-auto p-8">
                <div className="text-center mb-8">
                    <h1 className="text-5xl font-bold mb-4 bg-gradient-to-r from-red-500 to-red-700 bg-clip-text text-transparent">
                        Welcome to Vajra
                    </h1>
                    <p className="text-slate-400 text-lg">
                        Your Digital Fortress Against Cyber Threats
                    </p>
                </div>

                <div className="bg-slate-900/50 border border-slate-800 rounded-lg p-8">
                    <h2 className="text-2xl font-bold mb-6 text-center">Sign In to Continue</h2>

                    <button
                        onClick={handleGoogleSignIn}
                        disabled={isSigningIn}
                        className="w-full bg-white hover:bg-slate-100 text-black font-semibold py-3 px-6 rounded-lg flex items-center justify-center gap-3 transition-colors disabled:opacity-50 disabled:cursor-not-allowed"
                    >
                        {isSigningIn ? (
                            <>
                                <div className="w-5 h-5 border-2 border-black border-t-transparent rounded-full animate-spin" />
                                Signing in...
                            </>
                        ) : (
                            <>
                                <svg className="w-5 h-5" viewBox="0 0 24 24">
                                    <path
                                        fill="currentColor"
                                        d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                    />
                                    <path
                                        fill="currentColor"
                                        d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                    />
                                    <path
                                        fill="currentColor"
                                        d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                                    />
                                    <path
                                        fill="currentColor"
                                        d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                    />
                                </svg>
                                Continue with Google
                            </>
                        )}
                    </button>

                    <p className="text-sm text-slate-500 text-center mt-6">
                        By signing in, you agree to our Terms of Service and Privacy Policy
                    </p>
                </div>

                <div className="mt-8 text-center">
                    <p className="text-slate-400">
                        New to Vajra?{' '}
                        <span className="text-red-500 font-semibold">
                            Sign in to create your workspace
                        </span>
                    </p>
                </div>
            </div>
        </div>
    )
}
